class Point:
    """
    Class Point
    Attributes: x, y
    Methods: delta_x, delta_y
    """
    def __init__(self, x, y):
        """
        Constructor for the Point class
        Parameters:
            x (float) -- the x-coordinate of the point
            y (float) -- the y-coordinate of the point
        """
        self.x = x
        self.y = y
